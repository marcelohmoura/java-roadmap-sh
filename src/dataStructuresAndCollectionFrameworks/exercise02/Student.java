package dataStructuresAndCollectionFrameworks.exercise02;

public class Student {
    private String name;
    private int age;

    void setName(String name) {
        this.name = name;
    }
    String getName() {
        return name;
    }
    void setAge(int age) {
        this.age = age;
    }
    int getAge() {
        return age;
    }
}
