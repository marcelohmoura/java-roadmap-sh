package dataTypesAndVariables.exercise10;

public class Contact {
    private String name, phone, email;

    String getName() {
        return name;
    }
    String getPhone() {
        return phone;
    }
    String getEmail() {
        return email;
    }
    void setName(String name) {
        this.name = name;
    }
    void setPhone(String phone) {
        this.phone = phone;
    }
    void setEmail(String email) {
        this.email = email;
    }
}
